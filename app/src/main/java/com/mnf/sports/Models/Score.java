
package com.mnf.sports.Models;


public class Score {

    private String blue;
    private String green;
    private String yellow;
    private String red;

    /**
     * 
     * @return
     *     The blue
     */
    public String getBlue() {
        return blue;
    }

    /**
     * 
     * @param blue
     *     The blue
     */
    public void setBlue(String blue) {
        this.blue = blue;
    }

    /**
     * 
     * @return
     *     The green
     */
    public String getGreen() {
        return green;
    }

    /**
     * 
     * @param green
     *     The green
     */
    public void setGreen(String green) {
        this.green = green;
    }

    /**
     * 
     * @return
     *     The yellow
     */
    public String getYellow() {
        return yellow;
    }

    /**
     * 
     * @param yellow
     *     The yellow
     */
    public void setYellow(String yellow) {
        this.yellow = yellow;
    }

    /**
     * 
     * @return
     *     The red
     */
    public String getRed() {
        return red;
    }

    /**
     * 
     * @param red
     *     The red
     */
    public void setRed(String red) {
        this.red = red;
    }

}
