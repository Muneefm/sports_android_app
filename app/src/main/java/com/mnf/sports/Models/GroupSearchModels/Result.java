
package com.mnf.sports.Models.GroupSearchModels;


public class Result {

    private String name;
    private String year;
    private String cls;
    private String group;

    /**
     * 
     * @return
     *     The name
     */
    public String getName() {
        return name;
    }

    /**
     * 
     * @param name
     *     The name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * 
     * @return
     *     The year
     */
    public String getYear() {
        return year;
    }

    /**
     * 
     * @param year
     *     The year
     */
    public void setYear(String year) {
        this.year = year;
    }

    /**
     * 
     * @return
     *     The cls
     */
    public String getCls() {
        return cls;
    }

    /**
     * 
     * @param cls
     *     The cls
     */
    public void setCls(String cls) {
        this.cls = cls;
    }

    /**
     * 
     * @return
     *     The group
     */
    public String getGroup() {
        return group;
    }

    /**
     * 
     * @param group
     *     The group
     */
    public void setGroup(String group) {
        this.group = group;
    }

}
